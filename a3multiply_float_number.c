//C program to multiply two floating-point numbers
#include<stdio.h>
int  main()
{
    float a,b;
    printf("Enter the two float number:");
    scanf("%f %f",&a,&b);
    printf("the multiplication of %.2f and %.2f is %.2f",a,b,a*b);
    return 0;
}
