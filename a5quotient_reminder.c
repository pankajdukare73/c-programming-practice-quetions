//C program to find quotient and remainder of Two Integers
#include<stdio.h>
int  main()
{
    int a,b;
    printf("Enter the two number:");
    scanf("%d %d",&a,&b);

    printf("dividing %d and %d then quotient is %d and reminder is %d.",a,b,a/b,a%b);
    return 0;
}
